# Bài 2
a = int(input("Nhập vào a: "))
b = int(input("Nhập vào b: "))
# Tổng 
Tong = a + b
print("Tổng là: {}".format(Tong))
# Hiệu
Hieu = a - b
print("Hiệu là: {}".format(Hieu))
# Tích
Tich = a * b
print("Tích là: {}".format(Tich))
# Thương
Thuong = a / b
print("Thương là: ",round(Thuong,2))